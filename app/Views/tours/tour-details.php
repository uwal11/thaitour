        $('.quantity-container').each(function() {
            var $container = $(this);
            var $quantityDisplay = $container.find('.quantity');
            var $increaseBtn = $container.find('.increase');
            var $decreaseBtn = $container.find('.decrease');
            var quantity = 0;

            $increaseBtn.click(function() {
                quantity++;
                $quantityDisplay.text(quantity);
                $decreaseBtn.removeAttr('disabled');
            });

            $decreaseBtn.click(function() {
                if (quantity > 0) {
                    quantity--;
                    $quantityDisplay.text(quantity);
                }
                if (quantity === 0) {
                    $decreaseBtn.attr('disabled', true);
                }
            });
        });

        $(function() {
            // Initialize the datepicker
            $("#datepicker").datepicker({
                showOtherMonths: true,
                beforeShowDay: function(date) {
                    var day = date.getDate();

                    // Example logic for fully booked dates
                    var fullyBookedDates = [2, 9, 16, 30]; // Example of fully booked dates
                    if (fullyBookedDates.includes(day)) {
                        return [false, "", "예약마감"]; // Disabled dates
                    }

                    // Example logic for special rate dates
                    var specialRateDates = [24, 25, 26, 27, 28]; // Example of special rate dates
                    if (specialRateDates.includes(day)) {
                        return [true, "special-rate", "58,217원 (1,452바트)"]; // Special rate dates
                    }

                    return [true, "", ""];
                },
                onSelect: function(dateText, inst) {
                    // Handle date selection
                    $("td a").removeClass("selected");
                    $(inst.dpDiv).find("td a").each(function() {
                        if ($(this).text() === dateText.split("/")[1]) {
                            $(this).addClass("selected");
                        }
                    });
                }
            });
